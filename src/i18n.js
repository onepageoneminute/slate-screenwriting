export function getIdiomaGuardado() {
  try { return localStorage.getItem(CLAVE) } catch { return null }
}

let _idioma = getIdiomaGuardado() || 'en' // inglés por defecto; se cambia con el desplegable
const subs = new Set()

export function useIdioma() {
  const [idioma, setIdiomaEstado] = useState(_idioma)

  useEffect(() => {
    const fn = (v) => setIdiomaEstado(v)
    subs.add(fn)
    return () => subs.delete(fn)
  }, [])

  const setIdioma = useCallback((v) => {
    _idioma = v
    try { localStorage.setItem(CLAVE, v) } catch {}
    subs.forEach(fn => fn(v))
    window.electronAPI?.setIdioma?.(v) // que el corrector siga el idioma
  }, [])

  const t = useCallback((clave, params) => {
    const dicc = TRADUCCIONES[idioma || 'es'] || TRADUCCIONES.es
    let txt = dicc[clave] ?? TRADUCCIONES.es[clave] ?? clave
    if (params) for (const k in params) txt = txt.replace(`{${k}}`, params[k])
    return txt
  }, [idioma])

  return { idioma, setIdioma, t }
}
